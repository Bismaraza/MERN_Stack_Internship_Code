// Task 1 : Grade Calculator
let numbers = 85;

if (numbers >= 90) {
    console.log("Grade A");
}
else if (numbers >= 80) {
    console.log("Grade B");
}
else if (numbers >= 70) {
    console.log("Grade C");
}
else if (numbers >= 60) {
    console.log("Grade D");
}
else {
    console.log("Fail");
}

